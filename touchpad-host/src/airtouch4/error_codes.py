def format_error_code(code: int, show_type: int, code_map: dict[int, str]) -> str:
    mapped = code_map.get(code)
    if mapped:
        return mapped
    if show_type == 1 or code in (65534, 65535):
        return f"{code:X}"
    if show_type == 2:
        return str(code)
    return f"{code}(0x{code:X})"

def empty_error_description(code: int) -> str:
    if code == 65534:
        return "Error in the communication of the gateway with the main module."
    if code == 65535:
        return "Error in the communication of the gateway with the AC unit."
    return "Unknown error."

def describe_ac_error(brand_id: int | None, code: int | None) -> dict[str, object] | None:
    if code in (None, 0):
        return None
    code_int = int(code)
    brand_key = BRAND_TABLE_BY_ID.get(int(brand_id)) if brand_id is not None else None
    brand_name = BRAND_NAMES.get(brand_key or "", "")
    code_map = ERROR_CODE_MAPS.get(brand_key or "", {})
    show_type = 3
    prefix = brand_name
    if brand_key in {"me", "lg", "samsung", "haier"}:
        show_type = 2
    elif brand_key == "mhi":
        show_type = 2
    display_code = format_error_code(code_int, show_type, code_map)
    if brand_key == "mhi":
        display_code = f"E{display_code}"
    label = f"{prefix} Code:{display_code}" if prefix else f"Code: {display_code}"
    description = empty_error_description(code_int) if code_int in (65534, 65535) else ""
    return {
        "brand_id": brand_id,
        "brand": brand_name,
        "brand_key": brand_key,
        "code": code_int,
        "display_code": display_code,
        "label": label,
        "description": description,
    }
